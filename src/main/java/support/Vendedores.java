package support;

public class Vendedores {


    String filial;
    String empresaMatricula;
    String senha;


    public String getFilial() {
        return filial;
    }

    public String getEmpresaMatricula() {
        return empresaMatricula;
    }

    public String getSenha() {
        return senha;
    }


    public void setFilial(String filial) {
        this.filial = filial;
    }

    public void setEmpresaMatricula(String empresaMatricula) {
        this.empresaMatricula = empresaMatricula;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }
}
